        <p>Gangguan kesehatan mental yang terjadi selama pandemi dapat disebabkan oleh berbagai hal, seperti ketakutan terhadap 
            wabah, rasa terasing selama menjalani karantina, kesedihan dan kesepian karena jauh dari keluarga atau orang yang 
            dikasihi, kecemasan akan kebutuhan hidup sehari-hari, ditambah lagi kebingungan akibat informasi yang simpang siur.</p>
        <p>Hal-hal tersebut tidak hanya berdampak pada orang yang telah memiliki masalah kesehatan mental, seperti depresi atau 
            gangguan kecemasan umum, namun juga dapat memengaruhi orang yang sehat secara fisik dan mental.</p>
        <p>Beberapa kelompok yang rentan mengalami stres psikologis selama pandemi virus Corona adalah anak-anak, lansia, 
            dan petugas medis. Tekanan yang berlangsung selama pandemi ini dapat menyebabkan gangguan berupa:</p>
        <ul>
            <li>Ketakutan dan kecemasan yang berlebihan akan keselamatan diri sendiri maupun orang-orang terdekat</li>
            <li>Perubahan pola tidur dan pola makan</li>
            <li>Bosan dan stres karena terus-menerus berada di rumah, terutama pada anak-anak</li>
            <li>Sulit berkonsentrasi</li>
            <li>Penyalahgunaan alkohol dan obat-obatan</li>
            <li>Memburuknya kesehatan fisik, terutama pada penderita penyakit kronis, seperti diabetes dan hipertensi</li>
        </ul>
        <br>
        <h5>Tips Menjaga Kesehatan Mental Selama Pandemi Virus Corona</h5>
        <p>Berikut ini adalah beberapa hal yang dapat Anda lakukan untuk menjaga kesehatan mental selama pandemi virus Corona:</p>
        <h6>1.	Melakukan aktivitas fisik</h6>
        <p>Berbagai olahraga ringan, seperti lari kecil atau lompat di tempat, dapat Anda lakukan selama menjalani karantina di rumah. 
            Dengan melakukan aktivitas fisik, tubuh Anda akan memproduksi hormon endorfin yang dapat meredakan stres, mengurangi rasa 
            khawatir, dan memperbaiki mood Anda.Latihan peregangan dan pernapasan juga dapat membantu Anda untuk menenangkan diri. Jangan 
            lupa untuk berjemur di bawah sinar matahari pagi untuk meningkatkan sistem imun.</p>
        <h6>2.	Mengonsumsi makanan bergizi</h6>
        <p>Konsumsilah makanan yang mengandung protein, lemak sehat, karbohidrat, vitamin, mineral, dan serat. Beragam nutrisi tersebut 
            dapat Anda peroleh dari nasi dan cereal, buah-buahan, sayuran, makanan laut, daging, kacang-kacangan, serta susu. Bukan hanya 
            untuk menjaga kesehatan tubuh Anda, asupan nutrisi yang cukup juga dapat menjaga kesehatan mental Anda, baik secara langsung 
            maupun tidak langsung.</p>
        <h6>3.	Menghentikan kebiasaan buruk</h6>
        <p>Bila Anda seorang perokok, cobalah hentikan kebiasaan buruk tersebut mulai dari sekarang. Merokok akan meningkatkan risiko 
            Anda terinfeksi kuman penyakit, termasuk virus Corona. Selain itu, batasi juga konsumsi minuman beralkohol.Kebiasaan merokok 
            dan mengonsumsi minuman beralkohol dapat mengganggu kesehatan fisik maupun mental Anda.Kebiasaan buruk yang juga perlu dihentikan 
            adalah kurang beristirahat atau sering begadang. Jika kurang istirahat, Anda akan lebih mudah mengalami kecemasan dan mood Anda pun 
            akan lebih tidak stabil.</p>
        <h6>4.	Membuat rutinitas sendiri</h6>
        <p>Selama menjalani karantina di rumah, Anda bisa melakukan hobi atau aktivitas yang Anda sukai, misalnya memasak, membaca buku, atau 
            menonton film. Selain meningkatkan produktivitas, kegiatan tersebut juga dapat menghilangkan rasa jenuh.</p>
        <h6>5.	Lebih bijak memilah informasi</h6>
        <p>Batasi waktu Anda untuk menonton, membaca, atau mendengar berita mengenai pandemi, baik dari televisi, media cetak, maupun media 
            sosial untuk mengurangi rasa cemas.Meski begitu, jangan menutup diri sepenuhnya dari informasi yang penting. Pilah informasi yang 
            Anda terima secara kritis dan bijak. Dapatkan informasi mengenai pandemi virus Corona hanya dari sumber yang terpercaya.</p>
